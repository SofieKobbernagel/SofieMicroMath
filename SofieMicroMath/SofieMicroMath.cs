using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SofieMicroMath
{
    public class SofieMicroMath
    {

        /// <summary>
        /// Adds two numbers
        /// </summary>
        /// <param name="t1">First number</param>
        /// <param name="t2">Second number</param>
        /// <returns>the sum of the two numbers</returns>
        public int Add(int t1, int t2)
        {
            return t1 + t2;
        }

        public int Add3numbers(int t1, int t2, int t3)
        {
            return t1 + t2 + t3; //commentar fra online
        }
    }
}
